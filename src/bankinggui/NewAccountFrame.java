    private void openingBalFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_openingBalFieldKeyTyped
        char c = evt.getKeyChar();
        if(!((c>='0') && (c<='9') || (c==java.awt.event.KeyEvent.VK_BACK_SPACE) || (c==java.awt.event.KeyEvent.VK_DELETE)))
        {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_openingBalFieldKeyTyped

