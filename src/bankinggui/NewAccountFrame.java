    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
    boolean check = true;
    
    if(nameField.getText().length()==0)
        check=false;
    else if(dobField.getText().length()==0)
        check=false;
    else if(openingBalField.getText().length()==0)
        check=false;
    else if(dobField.getText().length()==0)
        check=false;
    else if(securityQField.getText().length()==0)
        check = false;
    else if ((new String(securityAField.getPassword()).length()==0))
        check = false;
    else if((new String(pinField.getPassword()).length())==0)
        check=false;
    else if(addressField1.getText().length()==0)
        check=false;
    else if(addressField2.getText().length()==0)
        check=false;
    else if(genderDropDown.getSelectedIndex()==0)
        check=false;
    else if(maritalStatusDropDown.getSelectedIndex()==0)
        check=false;
    
        if(!check)
        {
            getToolkit().beep();
            JOptionPane.showInputDialog(rootPane, "Entries not valid!","Please verify the entries");     
        }
        else if(!termsCheckBox.isSelected())
        {
            getToolkit().beep();
            JOptionPane.showInputDialog(rootPane, "Terms and Conditions!","Please accept our terms and conditions!");
        }
        else
        {
            String gender;
            boolean maritalStatus;
                if(maritalStatusDropDown.getSelectedIndex()==1) maritalStatus = false;
                else maritalStatus = true;
            
                if(genderDropDown.getSelectedIndex()==1) gender="M";
                else gender = "F";
                
            if(SqlHandler.writeRecord(accNoField.getText(), nameField.getText(), Float.parseFloat(openingBalField.getText()), 
                    User.encodePin(new String(pinField.getPassword())), securityQField.getText(), 
                    User.encodeSecurityA(new String(securityAField.getPassword())), gender, maritalStatus, 
                    dobField.getText(), addressField1.getText(), addressField2.getText(), addressField3.getText()))
            {
                JOptionPane.showInputDialog(rootPane, "Successful !", "Enjoy banking with us !");
                this.dispose();
            }
            else
            {
                JOptionPane.showInputDialog(rootPane, "SQL Error", "Problem with Table Creation. Contact developer.");
            }
        }
    }//GEN-LAST:event_submitButtonActionPerformed

